    /*Metodo para mostrar un siguiente jugador  en el apartado de seleccion*/
      public void nextPlayer() {

        if (contador < jugadoresAlmacen.size()) {

            jugador jgd;
            jgd = (jugador) jugadoresAlmacen.get(contador);
            txf_Nombre.setText(jgd.getNombre());
            txf_Edad.setText("" + jgd.getEdad());
            txf_Nacionalidad.setText(jgd.getNacionalidad());
            txfPatrocinador.setText(jgd.getMarca());
            contador += 1;
        } else {
            contador = 0;
        }
    }
    
    /*Metodo para mostrar un jugador anterior en el apartado de seleccion*/
    public void prevPlayer() {
        if (contador == 0) {
            contador = jugadoresAlmacen.size() - 1;
        } else {
            jugador jgd;
            jgd = (jugador) jugadoresAlmacen.get(contador);
            txf_Nombre.setText(jgd.getNombre());
            txf_Edad.setText("" + jgd.getEdad());
            txf_Nacionalidad.setText(jgd.getNacionalidad());
            txfPatrocinador.setText(jgd.getMarca());
            contador -= 1;
        }

    }
    
    
    /*Metodo para la eliminacion de un jugador*/
    public void deletePlayer() {
        try {
            // TODO add your handling code here:

            jugador jgd;
            jgd = (jugador) dflJugadores.getElementAt(listaJugadores.getSelectedIndex());

            Statement x = conexion.createStatement();

            x.executeUpdate("delete "
                    + "from jugadores "
                    + "where idJugador = " + jgd.getId());

            x.close();

            jugadoresAlmacen.remove(listaJugadores.getSelectedIndex());
            dflJugadores.removeElementAt(listaJugadores.getSelectedIndex());

        } catch (SQLException ex) {
            Logger.getLogger(frameApp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

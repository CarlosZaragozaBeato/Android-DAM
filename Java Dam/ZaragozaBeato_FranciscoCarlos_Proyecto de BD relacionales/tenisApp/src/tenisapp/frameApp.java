    /*Inserccion de un jugador*/
    public void create() {

        jugador jgd;
        int id, edad;
        String nombre, nacionalidad, marca;

        id = jugadoresAlmacen.size() + 1;
        nombre = "\"" + createNombre.getText() + "\"";
        edad = Integer.parseInt(createEdad.getText());
        nacionalidad = "\"" + createNacionalidad.getText() + "\"";
        marca = "\"" + createMarca.getText() + "\"";
   

        jgd = new jugador(id, edad, createNombre.getText(), createNacionalidad.getText(), createMarca.getText());

        jugadoresAlmacen.add(jgd);
        try {

            Statement x = conexion.createStatement();

            x.executeUpdate("INSERT INTO jugadores(idjugador,nombre,edad,nacionalidad,marcaPatrocinadora)"
                    + "values(" + id + ',' + nombre + ',' + edad + ',' + nacionalidad + ',' + marca + ")");

            x.close();

        } catch (SQLException ex) {
            Logger.getLogger(TenisApp.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            createEdad.setText("");
            createNombre.setText("");
            createNacionalidad.setText("");
            createMarca.setText("");
        }
    }

    
    /*Metodo para obtener todos los jugadores e incluirlos en una lista*/
    public void selectAll() {

        jugador jgd;
        int id, edad;
        String nombre, nacionalidad, patrocinador;
        jugadoresAlmacen.removeAll(jugadoresAlmacen);
        try {

            Statement x = conexion.createStatement(0, 0);

            ResultSet jugadores = x.executeQuery("select * from jugadores");

            while (jugadores.next()) {

                id = (int) jugadores.getObject("idJugador");
                edad = (int) jugadores.getObject("edad");

                nombre = (String) jugadores.getObject("nombre");
                nacionalidad = (String) jugadores.getObject("nacionalidad");
                patrocinador = (String) jugadores.getObject("marcaPatrocinadora");
                jgd = new jugador(id, edad, nombre, nacionalidad, patrocinador);

                jugadoresAlmacen.add(jgd);
            }

            jugadores.close();

            x.close();

        } catch (SQLException ex) {
            Logger.getLogger(TenisApp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


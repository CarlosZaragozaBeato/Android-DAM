    public void recuperarTodo(){
            listaEntrenadores.clear();
            listaEquipos.clear();
            FileReader fr = null;
            BufferedReader br = null;
            File fichero=null;

           
            String []trozos;
            
            String linea="",nombre,entrenador,nombreEntrenador;
            double puntos;
            
            int edad;
            
            
            equipo equi;
            Entrenador entr;
           
        try {
           
            fichero = new File("EquiposEntrenadores.txt");
            fr = new FileReader(fichero);
            br = new BufferedReader(fr);
            
            
            while((linea=br.readLine())!=null){
              trozos = linea.split(":");
              
                if(trozos[0].equalsIgnoreCase("equipo")){
                        
                        nombre = trozos[1];
                        entrenador = trozos[2];
                        puntos = Double.parseDouble(trozos[3]);
                        equi = new equipo(nombre,puntos, entrenador);
                        listaEquipos.addElement(equi); 
            }else if(trozos[0].equalsIgnoreCase("entrenador")){
                       
                        nombreEntrenador = trozos[1];
                        edad = Integer.parseInt(trozos[2]);
                        entr = new Entrenador(nombreEntrenador, edad);
                        listaEntrenadores.addElement(entr);
            }
                
               
            }
            
         
            
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(guiEquipos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(guiEquipos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    




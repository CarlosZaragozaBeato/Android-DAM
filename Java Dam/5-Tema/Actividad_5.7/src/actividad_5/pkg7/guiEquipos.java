    public void guardarXML(){
     
        
        try {
            DocumentBuilderFactory docFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder docBuilder = docFactory.newDocumentBuilder();
            
            doc = docBuilder.newDocument();
            Element rootElement = doc.createElement("Futbol");
            Element rootEquipo = doc.createElement("Equipos");
            Element rootEntrenadores = doc.createElement("Entrenadores");

            doc.appendChild(rootElement);
            rootElement.appendChild(rootEquipo);
            rootElement.appendChild(rootEntrenadores);
            
            for(int i=0;i<listaEquipos.getSize();i++){
                
                Element equipo1 = doc.createElement("Equipo");
                
                equipo e = (equipo) listaEquipos.get(i);
                
                Element nombre = doc.createElement("nombreEq");
                nombre.setTextContent(e.getNombre());
                equipo1.appendChild(nombre);
                
                Element puntos = doc.createElement("puntos");
                puntos.setTextContent(""+e.getPuntos());
                equipo1.appendChild(puntos);
                
                Element entrenador = doc.createElement("nombreEntrenador");
                entrenador.setTextContent(e.getEntrenador());
                equipo1.appendChild(entrenador);
                
                
                rootEquipo.appendChild(equipo1);       
            }
            
            
                for(int i=0;i<listaEntrenadores.getSize();i++){
                
                Element entrenador1 = doc.createElement("Entrenador");
                
                Entrenador e = (Entrenador) listaEntrenadores.get(i);
                
                Element nombre = doc.createElement("nombreEn");
                nombre.setTextContent(e.getNombre());
                entrenador1.appendChild(nombre);
                
                Element edad = doc.createElement("edad");
                edad.setTextContent(""+e.getEdad());
                entrenador1.appendChild(edad);
                
            
                
                
                rootEntrenadores.appendChild(entrenador1);       
            }
            
            
            
            
            TransformerFactory transformerFactory = TransformerFactory.newInstance();
            Transformer transformer = transformerFactory.newTransformer();
            DOMSource source = new DOMSource(doc);
         
            StreamResult result = new StreamResult(new File("equipos.xml"));             
            transformer.transform(source, result);
              
            
            
            
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(guiEquipos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (TransformerConfigurationException ex) {
            Logger.getLogger(guiEquipos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (TransformerException ex) {
            Logger.getLogger(guiEquipos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    

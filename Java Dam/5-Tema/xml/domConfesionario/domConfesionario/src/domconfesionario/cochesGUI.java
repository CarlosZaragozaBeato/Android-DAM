    private void itm_cargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itm_cargarActionPerformed
        dlmCoches.clear();
        try 
        {
            // 1º Creamos una nueva instancia de un fabrica de constructores de documentos.
            DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();

            // 2º A partir de la instancia anterior, fabricamos un constructor de documentos, que procesará el XML.
            DocumentBuilder db = dbf.newDocumentBuilder();

            // 3º Procesamos el documento (almacenado en un archivo) y lo convertimos en un árbol DOM.
            doc=db.parse("NuestrosCoches.xml");
        } 
        catch (Exception ex) 
        {
            System.out.println("¡Error! No se ha podido cargar el documento XML.");
        }
        
        //Obtenemos el nodo raiz
        //raiz = doc.getDocumentElement();
        
        NodeList nl = doc.getElementsByTagName("coche");
        Element co;
        
        for (int i = 0; i < nl.getLength(); i++) {
            Coche c=null;
            co = (Element)nl.item(i);
            c = obtenerCoche(co);
            dlmCoches.addElement(c);
        }
    }//GEN-LAST:event_itm_cargarActionPerformed

    private void itm_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itm_guardarActionPerformed
        
        try
        {
            DocumentBuilderFactory docFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder docBuilder = docFactory.newDocumentBuilder();
            
            doc = docBuilder.newDocument();
            Element rootElement = doc.createElement("Concesionario");
            doc.appendChild(rootElement);
            
            for (int i = 0; i < dlmCoches.getSize(); i++) 
            {
                Element coche1 = doc.createElement("Coche");
                Coche c = (Coche) dlmCoches.get(i);
                
                Attr e0 = doc.createAttribute("id");
                e0.setValue(""+c.getId());
                coche1.setAttributeNode(e0);
                
                Element e1 = doc.createElement("marca");
                e1.setTextContent(c.getMarca());
                coche1.appendChild(e1);
                
                Element e2 = doc.createElement("modelo");
                e2.setTextContent(c.getModelo());
                coche1.appendChild(e2);
                
                Element e3 = doc.createElement("cilindrada");
                e3.setTextContent(""+c.getCilindrada());
                coche1.appendChild(e3);
                
                rootElement.appendChild(coche1);
            }
            
            TransformerFactory transformerFactory = TransformerFactory.newInstance();
            Transformer transformer = transformerFactory.newTransformer();
            DOMSource source = new DOMSource(doc);   

            StreamResult result = new StreamResult(new File("equipos.xml"));
            transformer.transform(source, result);
  
        }
        catch (ParserConfigurationException pce)
        {
            pce.printStackTrace();
        }
        catch (TransformerException tfe)
        {
            tfe.printStackTrace();
        }
    }//GEN-LAST:event_itm_guardarActionPerformed


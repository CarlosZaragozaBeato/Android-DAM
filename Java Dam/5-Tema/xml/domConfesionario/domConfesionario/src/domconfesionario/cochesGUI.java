    public Coche obtenerCoche(Element c)
    {
        String sid, smarca, smodelo, scilindrada;
        int iid;
        double icilindrada;
        Element el;
        NodeList lista;
        Coche ccoche;
        
        iid = Integer.parseInt(c.getAttribute("id"));
        
        lista = c.getElementsByTagName("marca");
        el = (Element)lista.item(0);
        smarca = el.getTextContent();
        
        lista = c.getElementsByTagName("modelo");
        el = (Element)lista.item(0);
        smodelo = el.getTextContent();
        
        lista = c.getElementsByTagName("cilindrada");
        el = (Element)lista.item(0);
        scilindrada = el.getTextContent();
        icilindrada = Double.parseDouble(scilindrada);
        
        ccoche = new Coche(iid, smarca, smodelo, icilindrada);
        return ccoche;
    }
    

    /**
     * @param args the command line arguments
     */
    public void guardar() throws IOException, ClassNotFoundException {
        Alumno alu;
        Calificación cali;
        FileOutputStream fos;
        ObjectOutputStream oos;

      
                                                
        
        cali = new Calificación(Double.parseDouble(txf_bbd.getText()), Double.parseDouble(txf_Programacion.getText()), Double.parseDouble(txf_ende.getText()), Double.parseDouble(txf_sisi.getText()), Double.parseDouble(txf_fol.getText()), Double.parseDouble(txf_ing.getText()), Double.parseDouble(txf_lgm.getText()));
        alu = new Alumno(txf_dni.getText(), txf_nombre.getText(), txf_apellidos.getText(), Integer.parseInt(txf_movil.getText()), txf_email.getText(), cali);
      
        try {
            fos = new FileOutputStream(padre.getFichero());
            oos = new ObjectOutputStream(fos);

            
             padre.getAlmacenAlumnos().add(alu);
             
             for(int i=0;i< padre.getAlmacenAlumnos().size();i++){
                 oos.writeObject(padre.getAlmacenAlumnos().get(i));
             }
         
            JOptionPane.showMessageDialog(this, "Alumno guardado correctamente. ", "Informacion", JOptionPane.INFORMATION_MESSAGE);

          
        

   
        } catch (java.lang.NumberFormatException ex) {

        } catch (IOException ex) {

        }finally{
        txf_Programacion.setText(null);
        txf_apellidos.setText(null);
        txf_bbd.setText(null);
        txf_dni.setText(null);
        txf_email.setText(null);
        txf_ende.setText(null);
        txf_fol.setText(null);
        txf_ing.setText(null);
        txf_lgm.setText(null);
        txf_movil.setText(null);
        txf_nombre.setText(null);
        txf_sisi.setText(null);
        }

    }



    public void añadirPelicula() {

        
        Pelicula pr;
        
       

        try {
         
            /*Mejorable*/
            if ((txf_director.getText().equals("")) && (txf_titulo.getText().equals("")) && (txf_fecha.getText().equals(""))) {
                JOptionPane.showMessageDialog(this, "Debe escribir la fecha del estreno.", "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Debe escribir un título.", "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Debe escribir el nombre de un director.", "Error", JOptionPane.ERROR_MESSAGE);

            } else if ((txf_director.getText().equals("")) && (txf_titulo.getText().equals(""))) {
                JOptionPane.showMessageDialog(this, "Debe escribir un título.", "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Debe escribir el nombre de un director.", "Error", JOptionPane.ERROR_MESSAGE);

            } else if ((txf_director.getText().equals("")) && (txf_fecha.getText().equals(""))) {
                JOptionPane.showMessageDialog(this, "Debe escribir la fecha del estreno.", "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Debe escribir el nombre de un director.", "Error", JOptionPane.ERROR_MESSAGE);

            } else if ((txf_fecha.getText().equals("")) && (txf_titulo.getText().equals(""))) {
                JOptionPane.showMessageDialog(this, "Debe escribir la fecha del estreno.", "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Debe escribir un título.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (txf_director.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe escribir el nombre de un director.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (txf_fecha.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe escribir la fecha del estreno.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (txf_titulo.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe escribir un título.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                pr = new Pelicula(txf_titulo.getText(), txf_director.getText(), (String) cbox_categoria.getSelectedItem(), txf_fecha.getText(), (String) cbox_Imagen.getSelectedItem());
                dflPeliculas.addElement(pr);
                JOptionPane.showMessageDialog(null, "Elemento añadido con exito.", "Completed", JOptionPane.INFORMATION_MESSAGE);
            }
            
            
        } catch (Exception e) {
                       JOptionPane.showMessageDialog(null, "Error.", "Error", JOptionPane.ERROR_MESSAGE);

        }finally{
            txf_director.setText(null); 
            txf_titulo.setText(null); 
            txf_fecha.setText(null); 
        }

    }

    public void guardar() throws FileNotFoundException, IOException {

        Pelicula pr;

        JFileChooser fileChooser = new JFileChooser("./");
        int seleccion = fileChooser.showSaveDialog(this), contador = 0;
        File f = null;
        FileOutputStream fos;
        ObjectOutputStream oos;

        try {
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                f = fileChooser.getSelectedFile();
                fos = new FileOutputStream(f);
                oos = new ObjectOutputStream(fos);

                for (int i = 0; i < dflPeliculas.getSize(); i++) {
                    oos.writeObject(dflPeliculas.get(i));
                }

            }

            JOptionPane.showMessageDialog(null, "Datos guardados con exito." + f.getName(), "Guardados", JOptionPane.INFORMATION_MESSAGE);

        } catch (java.lang.NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Previamente, debe seleccionar un archivo de texto.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } finally {

            dflPeliculas.clear();
        }
    }

    public void abrir() throws FileNotFoundException, IOException {

        File f;
        Pelicula pl;
        dflPeliculas.clear();
        JFileChooser fChooser = new JFileChooser("./");
        int seleccion = fChooser.showSaveDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            f = fChooser.getSelectedFile();
            try {
                FileInputStream fis = new FileInputStream(f);
                ObjectInputStream ois = new ObjectInputStream(fis);

                while ((pl = (Pelicula) ois.readObject()) != null) {
                    dflPeliculas.addElement(pl);
                }

            } catch (FileNotFoundException ex) {

            } catch (IOException ex) {

            } catch (ClassNotFoundException ex) {

            }

        }

    }


    public void mostrarImagenes() throws IOException {

        Pelicula pr;
        pr = (Pelicula) dflPeliculas.get(lst_Peliculas.getSelectedIndex());
        String enlace = "images/" + pr.getImagen();

        try {
            panel_img.removeAll();
            BufferedImage portada = ImageIO.read(new File(enlace));
            JLabel pic = new JLabel(new ImageIcon(portada));

            txf_titulo2.setText(pr.getTitulo());
            txf_categoria2.setText(pr.getCategoria());
            txf_director2.setText(pr.getDirector());

            txf_fecha2.setText(pr.getFechaEstreno());

            panel_img.add(pic);
            panel_img.updateUI();
        } catch (Exception e) {

        }

    }


    public void evasion() {

        if (evaluarCiudadania(almacenarCiudadanos(), almacenarCiudadanosEvasores()) > 20) {
            try {
                double cantidadEvadida = 0;

                for (int i = 0; i < almacenarCiudadanosEvasores().size(); i++) {
                    CiudadanoEvasor ev = (CiudadanoEvasor) almacenarCiudadanosEvasores().get(i);
                    cantidadEvadida += ev.getCantidad();
                }
                txf_cantidadEvadida.setText(cantidadEvadida + "$");
                txf_PorEvasores.setText(df.format(evaluarCiudadania(almacenarCiudadanos(), almacenarCiudadanosEvasores())) + "%");
                throw new PuebloEvasor(1, "Pueblo Evasor");

            } catch (PuebloEvasor ex) {
                System.out.println(ex);

            }
        }

    }

    public double evaluarCiudadania(ArrayList listaCiudadanos, ArrayList listaEvasores) {

        txf_TotalCiudadanos.setText("" + listaCiudadanos.size());
        txf_TotalEvasores.setText("" + listaEvasores.size());

        double total = Double.parseDouble(txf_TotalCiudadanos.getText());
        double evasores = Double.parseDouble(txf_TotalEvasores.getText());
        double resultado = 0;

        resultado = (evasores / total) * 100;

        return resultado;
    }


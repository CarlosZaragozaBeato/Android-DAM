   public void guardarCiudadanosXml() throws ParserConfigurationException, TransformerConfigurationException, TransformerException{

            DocumentBuilderFactory docFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder docBuilder ;
            docBuilder = docFactory.newDocumentBuilder();
      
          

            doc = docBuilder.newDocument();
            Element rootElement = doc.createElement("ciudadanos");

            doc.appendChild(rootElement);

            for (int i = 0; i < dflCiudadanos.size(); i++) {
               
                Ciudadano c = (Ciudadano) dflCiudadanos.get(i);

                Element ciudadano1 = doc.createElement("ciudadano");

                Element nombre = doc.createElement("nombre");
                nombre.setTextContent(c.getNombre());
                ciudadano1.appendChild(nombre);

                Element provincia = doc.createElement("provincia");
                provincia.setTextContent(c.getProvincia());
                ciudadano1.appendChild(provincia);

                Element sueldo = doc.createElement("sueldo");
                sueldo.setTextContent("" + c.getSueldo());
                ciudadano1.appendChild(sueldo);

                Element hijos = doc.createElement("hijos");
                hijos.setTextContent("" + c.getHijos());
                ciudadano1.appendChild(hijos);

                rootElement.appendChild(ciudadano1);
            }

            TransformerFactory transformerFactory = TransformerFactory.newInstance();
            Transformer transformer = transformerFactory.newTransformer();
            DOMSource source = new DOMSource(doc);

            StreamResult result = new StreamResult(new File("ciudadanos.xml"));
             transformer.transform(source, result);
 
}

    public Ciudadano obtenerCiudadano(Element l) {

        String nombreCiudadano, provincia;
        int hijos;
        double sueldo;

        Element el = null;
        NodeList lista = null;

        Ciudadano ciudadano;

        lista = l.getElementsByTagName("nombre");
        el = (Element) lista.item(0);
        nombreCiudadano = el.getTextContent();

        lista = l.getElementsByTagName("provincia");
        el = (Element) lista.item(0);
        provincia = el.getTextContent();

        lista = l.getElementsByTagName("sueldo");
        el = (Element) lista.item(0);
        sueldo = Double.parseDouble(el.getTextContent());

        lista = l.getElementsByTagName("hijos");
        el = (Element) lista.item(0);
        hijos = Integer.parseInt(el.getTextContent());

        ciudadano = new Ciudadano(nombreCiudadano, provincia, sueldo, hijos);

        return ciudadano;

    }

    public void recuperarCiudadano() {

        try {

            DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();

            DocumentBuilder db = dbf.newDocumentBuilder();

            Document doc = db.parse("ciudadanos.xml");

            raiz = doc.getDocumentElement();

            NodeList nlCiudadano = doc.getElementsByTagName("ciudadano");

            Element elementoCiudadano;

            for (int i = 0; i < nlCiudadano.getLength(); i++) {

                Ciudadano ciu = null;

                elementoCiudadano = (Element) nlCiudadano.item(i);

                ciu = obtenerCiudadano(elementoCiudadano);

                dflCiudadanos.addElement(ciu);

            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }

    public void leerEvasores() {
        File f = new File("CIUDADANOS_EVASORES.txt");
        FileReader fr = null;
        String linea, nombre;
        double cantidad;

        try {
            fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            br.readLine();
            while ((linea = br.readLine()) != null) {
                String[] trozos = linea.split(";");
                nombre = trozos[0].substring(trozos[0].indexOf(":") + 1);
                cantidad = Integer.parseInt(trozos[1].substring(trozos[1].indexOf(":") + 1));
                CiudadanoEvasor ev1 = new CiudadanoEvasor(nombre, cantidad);
                dflCiudadanosIlegales.addElement(ev1);

            }
        } catch (IOException ex) {

        } finally {
            try {
                fr.close();
            } catch (IOException e2) {
            }
        }
    }


    public void filtrar() {
        String provincia="";
        provincia = txf_Filtrar.getText();
     

       
              ArrayList ciudadanosOrdenado = new ArrayList();
        dflFiltrar.clear();

        for (int i = 0; i < dflCiudadanos.getSize(); i++) {
            Ciudadano ciudadan = (Ciudadano) dflCiudadanos.getElementAt(i);
            if (ciudadan.getHijos() >= 1) {
                if (ciudadan.getProvincia().equalsIgnoreCase(provincia)) {
                    ciudadanosOrdenado.add(ciudadan);
                }
            }
        }

        Object[] arrayCiudadanosOrdenados = ciudadanosOrdenado.toArray();
        Arrays.sort(arrayCiudadanosOrdenados);
        for (Object o : arrayCiudadanosOrdenados) {
            dflFiltrar.addElement(o);
        }
        list_Ciudadanos.updateUI();    
        
        
    }


    private void btn_guardarCiudadanosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarCiudadanosActionPerformed
        try
        {
            DocumentBuilderFactory docFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder docBuilder = docFactory.newDocumentBuilder();
            
            doc = docBuilder.newDocument();
            Element rootElement = doc.createElement("Ciudadanos");
            doc.appendChild(rootElement);
            
            
            for (int i = 0; i < dlmCiudadanos.getSize(); i++) 
            {
                
                Element ciu = doc.createElement("Ciudadano");
                Ciudadano c = (Ciudadano) dlmCiudadanos.get(i);
                
                Element c1 = doc.createElement("nombre");
                c1.setTextContent(c.getNombre());
                ciu.appendChild(c1);

                Element c2 = doc.createElement("provincia");
                c2.setTextContent(""+c.getSueldo());
                ciu.appendChild(c2);
                
                Element c3 = doc.createElement("sueldo");
                c3.setTextContent(""+c.getSueldo());
                ciu.appendChild(c3);
                
                Element c4 = doc.createElement("hijos");
                c4.setTextContent(""+c.getHijos());
                ciu.appendChild(c4);
                
                rootElement.appendChild(ciu);
            }
                
            TransformerFactory transformerFactory = TransformerFactory.newInstance();
            Transformer transformer = transformerFactory.newTransformer();
            DOMSource source = new DOMSource(doc);   

            StreamResult result = new StreamResult(new File("ciudadanosGUARDAR.xml"));
            transformer.transform(source, result);      
        }
        catch (ParserConfigurationException pce)
        {
            pce.printStackTrace();
        }
        catch (TransformerException tfe)
        {
            tfe.printStackTrace();
        }
    }//GEN-LAST:event_btn_guardarCiudadanosActionPerformed

    public double evaluarCiudadania(ArrayList listaCiudadanos, ArrayList listaEvasores)
    {
        double resultado=0;
        
        txf_numCiuTotales.setText(""+contadorCiudadanos);
        txf_numCiuEvasores.setText(""+contadorEvasores);
        
        resultado = (contadorEvasores*100)/contadorCiudadanos;
       
        try
        {
            if (resultado > 20)
            {
                throw new EPuebloEvasor(resultado, cantidadTotalEvadida);
            } 
        }

        catch (EPuebloEvasor ex)
        {
            txf_cantidadEvadida.setText(resultado+", "+cantidadTotalEvadida);
        }
        
        return resultado;
        
    }



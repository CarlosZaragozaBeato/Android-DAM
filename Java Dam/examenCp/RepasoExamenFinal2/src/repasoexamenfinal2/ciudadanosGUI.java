    public Ciudadano obtenerCiudadano(Element ciu)
    {
        Ciudadano c = null;
        String nombre, provincia, sueldo, hijos;
        int isueldo, ihijos;
        
        
        NodeList lista = null;
        Element el;
        
        
        //NOMBRE 
        lista = ciu.getElementsByTagName("nombre");
        el = (Element) lista.item(0);
        nombre = el.getTextContent();
        
        //PROVINCIA
        lista = ciu.getElementsByTagName("provincia");
        el = (Element) lista.item(0);
        provincia = el.getTextContent();
        
        //SUELDO
        lista = ciu.getElementsByTagName("sueldo");
        el = (Element) lista.item(0);
        sueldo = el.getTextContent();
        isueldo = Integer.parseInt(sueldo);
        
        //HIJOS
        lista = ciu.getElementsByTagName("hijos");
        el = (Element) lista.item(0);
        hijos = el.getTextContent();
        ihijos = Integer.parseInt(hijos);
        

        c = new Ciudadano(nombre, provincia, isueldo, ihijos);

        return c;
    }
    

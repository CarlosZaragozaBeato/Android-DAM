    @Override
    public int calcularAyudasPorHijos(ArrayList lista, int numeroMinimoHijos, String provincia)
    {
       int contador=0;
             
        for (int i = 0; i < lista.size(); i++) 
        {
            Ciudadano c = (Ciudadano) lista.get(i);
            
            if (c.getHijos() >= numeroMinimoHijos && c.getProvincia().equals(provincia))
            {
                contador++;
            }               
        }
        
        return contador;
    }
    

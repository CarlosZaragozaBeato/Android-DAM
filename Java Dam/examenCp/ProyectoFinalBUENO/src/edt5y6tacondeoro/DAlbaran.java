    public void guardarEnFicheroTexto()
    {
        FileWriter fw=null;
        PrintWriter pw=null;
        

        File f= new File("ALBARAN"+alb.getSocio().getNombre().toUpperCase()+".txt");
               
        try
        {
            fw = new FileWriter(f);
            pw = new PrintWriter(fw);


            pw.println("Nombre: "+alb.getSocio().getNombre());
            pw.println("Domicilio: "+alb.getSocio().getDireccion());
            pw.println("Poblacion: "+alb.getSocio().getPoblacion());
            pw.println("Cantidad: "+cogerCantidad());
            pw.println("Importe: "+df.format(cogerImporte()));
            
        }
        catch(IOException ex)
        {
            System.out.println(ex.getMessage());
        }
        finally
        {
            try
            {
                fw.close();
            }
            catch(Exception e2)
            {
                e2.printStackTrace();
            }
        }
    
    }
    

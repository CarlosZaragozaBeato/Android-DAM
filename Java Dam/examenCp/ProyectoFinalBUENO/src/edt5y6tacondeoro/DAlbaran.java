    public double cogerImporte()
    {
        Articulo articulo=null;
        
        double importeTotal=0;
        
        for (int i = 0; i < padre.p.getLineasPedidos().size(); i++) 
        {
            articulo = padre.p.getLineasPedidos().get(i).getArticulo();
            
            importeTotal += articulo.getPrecio()*padre.p.getLineasPedidos().get(i).getCantidad();                                   
        }
        importeTotal = importeTotal*1.21;
        
        return importeTotal;
    }
    
    public double cogerCantidad()
    {
        Articulo articulo=null;
        
        int cantidadTotal=0;
        
        for (int i = 0; i < padre.p.getLineasPedidos().size(); i++) 
        {
            articulo = padre.p.getLineasPedidos().get(i).getArticulo();
            
            cantidadTotal += padre.p.getLineasPedidos().get(i).getCantidad();
                        
        }
        
        return cantidadTotal;
    }


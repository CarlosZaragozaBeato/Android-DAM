    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Pedido p1 = new Pedido();
        String direccion = null;
        int numped = Integer.parseInt(txt_idpedido.getText());
        for (int i = 0; i < padre.listaPedidos.size(); i++) {
            p1 = padre.listaPedidos.get(i);
            if (p1.getIdPedido() == numped) {
                direccion = p1.getSocio().getPoblacion();
            }

        }
        String nomAlmacen = "Null";
        String nombreruta = comprobarRuta(direccion);
        if(nombreruta.equalsIgnoreCase("Norte")){
             nomAlmacen = "A";
        }else{
            nomAlmacen = "B";
        }
        
        padre.p.setSocio(p1.getSocio());
        padre.p.setIdPedido(p1.getIdPedido());
        
        

        
        JOptionPane.showMessageDialog(this, "El pedido numero: " + numped + " se va a mandar a la ruta: " + nombreruta+" almacen "+nomAlmacen, "Ruta", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed


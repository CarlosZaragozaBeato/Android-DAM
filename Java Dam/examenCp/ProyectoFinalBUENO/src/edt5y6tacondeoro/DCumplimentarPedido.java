    public void mostrarArticulos() {

        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        tbl_pedidos.updateUI();

        int pedido = lst_pedidos.getSelectedIndex();

        Pedido ped1 = (Pedido) dlm.getElementAt(pedido);

        ArrayList<LineaPedido> listaPedidos = ped1.getLineasPedidos();

        for (int i = 0; i < listaPedidos.size(); i++) {
            LineaPedido linea1;

            linea1 = listaPedidos.get(i);

            Articulo art = new Articulo();

            art = linea1.getArticulo();

            String datos[] = new String[4];
            double total;

            datos[0] = art.getNombre();
            datos[1] = "" + art.getPrecio();
            datos[2] = art.getDescripcion();
            datos[3] = "" + linea1.getCantidad();

            dtm.addRow(datos);

            tbl_pedidos.updateUI();
        }
    }


    public String comprobarRuta(String direccion) {
        Ruta r1;
        String rutadireccion = null;
        for (int i = 0; i < padre.listaRutas.size(); i++) {
            r1 = padre.listaRutas.get(i);
            String[] area = r1.getAreaInfluencia();
            for (int j = 0; j < area.length; j++) {
                String nombrearea = area[j];
                if (nombrearea.equals(direccion)) {
                    rutadireccion = r1.getNombre();

                }
            }
        }
        return rutadireccion;
    }


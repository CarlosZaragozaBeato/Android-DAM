    public void cargarTablaProductos() {
        int tamano = 0;
        tamano = padre.listaArticulos.size();
        for (int i = 0; i < tamano; i++) {
            Articulo a = padre.listaArticulos.get(i);
            String[] objetos = {a.getNombre(), Float.toString(a.getPrecio())};
            dtmTabla.addRow(objetos);
        }
    }

    public void eventoRaton() {
        int index = tbl_tabla.getSelectedRow();
        int index1 = tbl_tabla.getSelectedColumn();
        String nombre = (String) tbl_tabla.getValueAt(index, 0);
        float precio = Float.valueOf((String) tbl_tabla.getValueAt(index, 1));
        Articulo art = new Articulo(nombre, precio);
        lbl_Nombre.setText(art.getNombre());
        lbl_Precio.setText(Float.toString(art.getPrecio()));
    }

    public void anadirLP() {
        String nombre = (String) lbl_Nombre.getText();
        float precio = Float.valueOf((String) lbl_Precio.getText());
        Articulo art = new Articulo(nombre, precio);
        int cantidad = Integer.valueOf((String) txt_cantidad.getText());

        LineaPedido lp = new LineaPedido(art, cantidad);
        lpedidos.add(lp);
    }

    public void anadirLP2() {
        String nombre = (String) tbl_tabla.getValueAt(tbl_tabla.getSelectedRow(), 0);
        int precio = Integer.parseInt((String) tbl_tabla.getValueAt(tbl_tabla.getSelectedRow(), 1));
        Articulo art = new Articulo(nombre, precio);
        LineaPedido lp = new LineaPedido(art, Integer.valueOf((String) txt_cantidad.getText()));
        lpedidos.add(lp);
    }

    public void finalizarPedido() {

        Socio s = padre.saberSocio();
        Pedido p = new Pedido(s, lpedidos);
        padre.nuevoPedido(p);
    }


    private void lbl_incluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_incluirMouseClicked
        
        if (txf_nombreCliente.getText().equals("") || txf_apellidosCliente.getText().equals(""))
        {
           JOptionPane.showMessageDialog(null, "Debe rellenar los campos","Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {       
            try
            {
                int res=0;
                PreparedStatement ps = null;
                Connection conex = padre.hazConexion();

                ps = conex.prepareStatement("INSERT INTO CLIENTES(nombre,apellidos) VALUES (?,?)");
                ps.setString(1, txf_nombreCliente.getText());
                ps.setString(2, txf_apellidosCliente.getText());

                res = ps.executeUpdate();

                if (res>0)
                {
                    JOptionPane.showMessageDialog(null, "Cliente añadido correctamente");
                    txf_nombreCliente.setText("");
                    txf_apellidosCliente.setText("");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "ERROR. No se añadió al cliente");
                }
                ps.close();
                conex.close();


            }
            catch(SQLException ex)
            {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_lbl_incluirMouseClicked


    private void lbl_anadirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_anadirMouseClicked
        if (txf_fecha.getText().equals("") || txf_comensales.getText().toString().equals("") || txf_idcliente.getText().toString().equals("") || txf_idmesa.getText().toString().equals(""))
        {
           JOptionPane.showMessageDialog(null, "Debe rellenar los campos","Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else
        {       
            try
            {
                int res=0;
                PreparedStatement ps = null;
                Connection conex = padre.hazConexion();

                ps = conex.prepareStatement("INSERT INTO RESERVAS(fecha,ctdcomensales,idcliente,idmesa) VALUES (?,?,?,?)");
                ps.setString(1, txf_fecha.getText());
                ps.setString(2, txf_comensales.getText());
                ps.setString(3, txf_idcliente.getText());
                ps.setString(4, txf_idmesa.getText());

                res = ps.executeUpdate();

                if (res>0)
                {
                    JOptionPane.showMessageDialog(null, "Reserva aÃ±adida correctamente");
                    txf_fecha.setText("");
                    txf_comensales.setText("");
                    txf_idcliente.setText("");
                    txf_idmesa.setText("");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "ERROR. No se aÃ±adiÃ³ la reserva");
                }
                ps.close();
                conex.close();


            }
            catch(SQLException ex)
            {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_lbl_anadirMouseClicked


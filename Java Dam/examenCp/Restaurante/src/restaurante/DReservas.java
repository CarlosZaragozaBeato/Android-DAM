    public void eliminarReserva()
    {
        Connection conex = hazConexion5();
        int fila = tbl_reservas.getSelectedRow();
        String valor = tbl_reservas.getValueAt(fila, 0).toString();
        
        try 
        {
            PreparedStatement eliminar = conex.prepareStatement("DELETE FROM reservas WHERE idreserva='"+valor+"'");
            eliminar.executeUpdate();
            dtmReservas.removeRow(tbl_reservas.getSelectedRow());
            
            JOptionPane.showMessageDialog(null, "Reserva eliminada correctamente","Informacion",JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(null, "No se pudo eliminar la reserva","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void actualizarReserva()
    {
        int fila = tbl_reservas.getSelectedRow();
        int id = Integer.parseInt(this.tbl_reservas.getValueAt(fila,0).toString());
        
        String fecha = tbl_reservas.getValueAt(fila, 2).toString();
        
        try
        {
            Connection conex = hazConexion5();
            
            PreparedStatement st = conex.prepareStatement("UPDATE reservas SET fecha='"+fecha+"'"
                    + "                                    WHERE idreserva='"+id+"'");
            st.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Reserva actualizada correctamente");
            
        }
        catch(SQLException ex)
        {
             JOptionPane.showMessageDialog(null, "ERROR. No se pudo actualizar la reserva");
        }
    }

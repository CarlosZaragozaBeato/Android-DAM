    public void mostrarReservas(int opBuscar, String valor)
    {
        dtmReservas.setRowCount(0);
        
        String codsql=null;
        
        if (opBuscar == 0 && valor == null)
        {
            codsql = "SELECT * FROM RESERVAS";
        }
        else
        {
            if (opBuscar == 1 && valor != null)
            {
                codsql = "SELECT * FROM RESERVAS WHERE idreserva = '"+valor+"'";
            }
            else
            {
                if (opBuscar == 2 && valor !=null)
                {
                    codsql = "SELECT * FROM RESERVAS WHERE fecha = '"+valor+"'";
                }
                else
                {
                    if (opBuscar == 3 && valor !=null)
                    {
                        codsql = "SELECT * FROM RESERVAS WHERE idcliente ='"+valor+"'";
                    }
                    else
                    {
                        codsql = "SELECT * FROM RESERVAS";
                    }
                }               
            }
        }
        
        try
        {
            Connection conex = hazConexion5();
        
        
            Statement s = conex.createStatement();
        
            ResultSet rs = s.executeQuery(codsql);
            
            
            while (rs.next())
            {
                Object [] fila = new Object[5];
                
                for (int i=0;i<5;i++)
                {
                    fila[i] = rs.getObject(i+1);
                    
                }
                dtmReservas.addRow(fila);
            }
        }
        catch (SQLException ex)
        {
            System.out.println(ex.getMessage());
        }
    }
    

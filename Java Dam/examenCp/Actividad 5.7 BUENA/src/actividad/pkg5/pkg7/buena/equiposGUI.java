    public Equipo obtenerEquipo(Element equ) 
    {
        Equipo e = null;
        String nombreEquipo, puntos, nombreEntrenador;
        int ipuntos;
        NodeList lista = null;
        Element el;
        
        lista = equ.getElementsByTagName("NombreEquipo");
        el = (Element) lista.item(0);
        nombreEquipo = el.getTextContent();

        
        lista = equ.getElementsByTagName("NombreEntrenador");
        el = (Element) lista.item(0);
        nombreEntrenador = el.getTextContent();
        

        lista = equ.getElementsByTagName("Puntos");
        el = (Element) lista.item(0);
        puntos = el.getTextContent();
        ipuntos = Integer.parseInt(puntos);
        

        e = new Equipo(nombreEquipo, nombreEntrenador, ipuntos);

        return e;
    }
    

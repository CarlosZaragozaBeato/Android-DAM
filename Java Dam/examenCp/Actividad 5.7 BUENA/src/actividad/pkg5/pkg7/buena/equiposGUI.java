    private void itm_guardarXMLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itm_guardarXMLActionPerformed
        try
        {
            DocumentBuilderFactory docFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder docBuilder = docFactory.newDocumentBuilder();
            
            doc = docBuilder.newDocument();
            Element rootElement = doc.createElement("Liga");
            Element rootEquipos = doc.createElement("Equipos");
            Element rootEntrenadores = doc.createElement("Entrenadores");
            doc.appendChild(rootElement);
            
            rootElement.appendChild(rootEquipos);
            rootElement.appendChild(rootEntrenadores);
            
            for (int i = 0; i < dlmEquipos.getSize(); i++) 
            {
                
                Element equipo = doc.createElement("Equipo");
                Equipo e = (Equipo) dlmEquipos.get(i);
                
                Element e1 = doc.createElement("NombreEquipo");
                e1.setTextContent(e.getNombreEquipo());
                equipo.appendChild(e1);

                Element e2 = doc.createElement("NombreEntrenador");
                e2.setTextContent(e.getNombreEntrenador());
                equipo.appendChild(e2);
                
                Element e3 = doc.createElement("Puntos");
                e3.setTextContent(""+e.getPuntos());
                equipo.appendChild(e3);
                
                rootEquipos.appendChild(equipo);
                         
            }
            
            for (int i = 0; i < dlmEntrenadores.getSize(); i++) 
            {
                Element entrenador = doc.createElement("Entrenador");
                Entrenador e = (Entrenador) dlmEntrenadores.get(i);
                rootEntrenadores.appendChild(entrenador);
                
                Element e1 = doc.createElement("Nombre");
                e1.setTextContent(e.getNombre());
                entrenador.appendChild(e1);
                
                Element e2 = doc.createElement("Edad");
                e2.setTextContent(""+e.getEdad());
                entrenador.appendChild(e2);
                
                rootEntrenadores.appendChild(entrenador);
            }
            
            TransformerFactory transformerFactory = TransformerFactory.newInstance();
            Transformer transformer = transformerFactory.newTransformer();
            DOMSource source = new DOMSource(doc);   

            StreamResult result = new StreamResult(new File("equipos.xml"));
            transformer.transform(source, result);       
        }
        catch (ParserConfigurationException pce)
        {
            pce.printStackTrace();
        }
        catch (TransformerException tfe)
        {
            tfe.printStackTrace();
        }
    }//GEN-LAST:event_itm_guardarXMLActionPerformed


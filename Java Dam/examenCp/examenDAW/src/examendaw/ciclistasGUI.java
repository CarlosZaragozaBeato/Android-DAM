    public void leerTexto(){
        FileReader fr=null;
        BufferedReader br=null;
        String linea =null;
        String []trozos;
        dlmCiclistas.clear();
        
        try
        {
            fr = new FileReader("ciclistas.txt");
            br = new BufferedReader(fr);
            
            while((linea = br.readLine())!=null)
            {
                    trozos = linea.split(";");
                    Ciclista c = new Ciclista(trozos[0],trozos[1],trozos[3],Integer.parseInt(trozos[2].trim()), Integer.parseInt(trozos[4]));
                    
                    dlmCiclistas.addElement(c);
                    
                    if (dlmEquipos.contains(c.getEquipo()))
                    {
                        
                    }
                    else
                    {
                        dlmEquipos.addElement(c.getEquipo()); 
                    }

                    lst_ciclistas.updateUI();
                    lst_equipos.updateUI();
            }
        }
        catch(FileNotFoundException ex)
        {
            System.out.println(ex.getMessage());
        }
        catch(IOException ex)
        {
            System.out.println(ex.getMessage());
        }
        finally
        {
            try
            {
                fr.close();
            }
            catch(Exception ex)
            {
                ex.printStackTrace();
            }
        }
    }

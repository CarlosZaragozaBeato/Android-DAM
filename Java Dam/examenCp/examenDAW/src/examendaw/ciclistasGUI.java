    public void MasDopado(){
        Ciclista[] almacen = new Ciclista[dlmCiclistas.size()];
        dlmCiclistas.copyInto(almacen);
        
        Arrays.sort(almacen, new ObtenerCiclistaMasDopado());
        
        for(int i=0; i<almacen.length;i++)
        {
            txf_ciclistaMasDopado.setText(""+almacen[i]);
        }
    }
    

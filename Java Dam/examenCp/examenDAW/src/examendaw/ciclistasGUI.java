    public void pesoMedio(){
          int cont=0, acu=0;
          double media=0;
        
        for (int i = 0; i < dlmCiclistas.getSize(); i++) {
            Ciclista c = (Ciclista) dlmCiclistas.get(i);
            
            acu += c.getPeso();
            cont++;
            
        }       
        media = acu/cont;
        txf_pesoMedio.setText(""+media);
    }

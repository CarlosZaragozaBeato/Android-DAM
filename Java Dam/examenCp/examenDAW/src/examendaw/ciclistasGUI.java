    public void equipoMixto(){
          for (int i = 0; i < dlmEquipos.size(); i++) 
        {
            int hombre=0, mujer=0;
            
            String e = (String) dlmEquipos.getElementAt(i);
            
            for (int j = 0; j < dlmCiclistas.size(); j++) 
            {
                Ciclista c = (Ciclista) dlmCiclistas.getElementAt(j);
                
                if (c.getEquipo().equals(e))
                {
                    if (c.getSexo().equals("Hombre"))
                    {
                        hombre++;
                    }
                    else
                    {
                        mujer++;
                    }
                }                
            }
            
            if (hombre > 0 && mujer > 0)
            {
                dlmEquiposMixtos.addElement(e);
            }            
        }
    }

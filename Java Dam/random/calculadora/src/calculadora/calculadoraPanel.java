    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        // TODO add your handling code here:

        num2 = Double.parseDouble(principalPanel.getText());

        switch (operator) {
            case '+':
                result = num1 + num2;
                break;

            case '-':
                result = num1 - num2;
                break;

            case '/':
                result = num1 / num2;
                break;

            case '*':
                result = num1 * num2;
                break;

         
            
            
                  
        }
    
                principalPanel.setText(result+"");
                num1 = result;

    }//GEN-LAST:event_igualActionPerformed

